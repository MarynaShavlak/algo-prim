"""Дані прикладів — єдине джерело правди для трьох навчальних графів.

Кожен граф — :class:`_common.GraphExample`: список ребер ``(u, v, вага)``
неорієнтованого графа + координати ``{вершина: (x, y)}`` для малювання.
Приклади лише імпортують потрібний граф, тож ребра й координати визначені
рівно один раз.

* :data:`ABCDEF` — **той самий граф ``A–F``**, що й у сусідньому проєкті про
  алгоритм Флойда–Воршала (ті самі ребра, ваги й координати), але тут він
  **неорієнтований**: МОД визначено саме для неорієнтованих графів, тож
  стрілки знято, а ваги збережено.
* :data:`ABCDEFG` — граф ``A–G`` із демонстрації базової реалізації (лекційний
  приклад). На ньому добре видно «ліниве видалення»: три застарілі ребра
  поспіль.
* :data:`ISLANDS` — незв'язний граф із двох «островів» для розділу про
  обмеження алгоритму.
"""

from __future__ import annotations

from typing import Dict, List

from _common import GraphExample

from prim_mst.i18n import t
from prim_mst.style import NEUTRAL_GRAY, NODE_JUST_ADDED_EDGE, TREE_EDGE

#: Основний приклад: граф ``A–F`` із проєкту про Флойда–Воршала, неорієнтований.
ABCDEF = GraphExample(
    edges=[
        ("A", "B", 3),
        ("B", "C", 1),
        ("C", "D", 7),
        ("C", "F", 2),
        ("D", "E", 2),
        ("E", "F", 3),
    ],
    positions={"A": (3.4, 2.6), "B": (3.0, 1.7), "C": (2.0, 1.7),
               "D": (2.1, 0.0), "E": (0.9, 1.7), "F": (0.4, 2.7)},
)

#: Граф ``A–G`` із демонстрації базової реалізації (7 вершин, 11 ребер).
ABCDEFG = GraphExample(
    edges=[
        ("A", "B", 7),
        ("A", "D", 5),
        ("B", "C", 8),
        ("B", "D", 9),
        ("B", "E", 7),
        ("C", "E", 5),
        ("D", "E", 15),
        ("D", "F", 6),
        ("E", "F", 8),
        ("E", "G", 9),
        ("F", "G", 11),
    ],
    positions={"A": (0.0, 2.1), "B": (2.2, 2.7), "C": (4.3, 2.0),
               "D": (0.9, 0.7), "E": (2.6, 1.3), "F": (2.1, -0.6),
               "G": (4.4, -0.1)},
)

#: Незв'язний граф: «острови» {M, N, O} та {P, Q} — обмеження Прима.
ISLANDS = GraphExample(
    edges=[
        ("M", "N", 2),
        ("N", "O", 4),
        ("M", "O", 7),
        ("P", "Q", 3),
    ],
    positions={"M": (0.0, 1.5), "N": (1.4, 2.2), "O": (1.2, 0.5),
               "P": (3.2, 2.0), "Q": (4.0, 0.8)},
)


def cable_plans() -> List[Dict[str, object]]:
    """Три плани прокладання кабелю для графа :data:`ABCDEF` (розділ «Інтуїція»).

    Підписи будуються в момент виклику, щоб :func:`prim_mst.i18n.t` встигла
    дізнатися поточну мову.
    """
    edges = ABCDEF.edges
    tree_not_minimal = [("A", "B", 3), ("B", "C", 1), ("C", "D", 7),
                        ("C", "F", 2), ("D", "E", 2)]
    mst = [("A", "B", 3), ("B", "C", 1), ("C", "F", 2), ("E", "F", 3), ("D", "E", 2)]
    return [
        {"edges": list(edges), "color": NEUTRAL_GRAY,
         "caption": t("прокласти всі 6 трас: 18 — цикли, зайві витрати")},
        {"edges": tree_not_minimal, "color": NODE_JUST_ADDED_EDGE,
         "caption": t("дерево (без циклів), але не найдешевше: 15")},
        {"edges": mst, "color": TREE_EDGE,
         "caption": t("мінімальне остовне дерево: 11 ✓")},
    ]
