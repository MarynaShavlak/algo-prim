"""Єдина візуальна тема для всіх візуалізацій: палітра кольорів і параметри matplotlib.

Усі кольори зведено в одне місце іменованими константами, щоб змінити вигляд
рисунків можна було з однієї точки. Палітра свідомо успадковує колірну мову
сусіднього проєкту про Флойда–Воршала (синій = «у фокусі», зелений = «прийнято /
у дереві», червоний = «відхилено / акцент»), тож обидва розбори читаються
однаково.
"""

from __future__ import annotations

import matplotlib.pyplot as plt

# ---------------------------------------------------------------------------
# Параметри фігур
# ---------------------------------------------------------------------------
#: DPI для всіх збережених/показаних фігур.
FIGURE_DPI: int = 110
#: DPI для GIF-анімацій (нижчий за статичні рисунки — менший розмір файлу).
GIF_DPI: int = 90
#: Базовий розмір шрифту matplotlib.
BASE_FONT_SIZE: int = 11
#: Сімейство шрифтів (містить кирилицю та символи ✓/✗).
FONT_FAMILY: str = "DejaVu Sans"

# ---------------------------------------------------------------------------
# Палітра: граф
# ---------------------------------------------------------------------------
#: Нейтральна вершина (рисунки «просто граф», без стану алгоритму).
NODE_NEUTRAL = "#1f77b4"
#: Вершина, яка ще НЕ в дереві (сіра, «не приєднана»).
NODE_OUT = "#90A4AE"
#: 🟩 вершина, яка вже в МОД (дерево росте зеленим).
NODE_IN = "#2E7D32"
#: 🟧 обведення вершини, щойно доданої на цьому кроці.
NODE_JUST_ADDED_EDGE = "#F57C00"
#: 🟩 ребро, що вже належить МОД.
TREE_EDGE = "#2E7D32"
#: 🔴 ребро, щойно зняте з черги (акцент поточного кроку).
POPPED_EDGE = "#D32F2F"
#: 🟦 ребро, що зараз лежить у черзі з пріоритетами (пунктир).
FRINGE_EDGE = "#1976D2"
#: Звичайне (поза чергою і деревом) ребро графа та допоміжний сірий текст.
NEUTRAL_GRAY = "#9E9E9E"
#: Бліде «відкинуте» ребро на підсумкових рисунках (не ввійшло в МОД).
REJECTED_EDGE = "#CFCFCF"
#: Колір підсвіченого розрізу (заливка множини S — вершин дерева).
CUT_FILL = "#E8F5E9"
#: Обведення області розрізу.
CUT_EDGE = "#66BB6A"

# ---------------------------------------------------------------------------
# Палітра: панель черги з пріоритетами
# ---------------------------------------------------------------------------
#: 🟨 рядок черги, який щойно зняли (heappop) — у фокусі кроку.
QUEUE_POP_FILL = "#FFF3C4"
#: 🟩 щойно доданий до черги рядок (heappush на цьому кроці).
QUEUE_NEW_FILL = "#C8E6C9"
#: Застарілий рядок (ребро веде у вершину, що ВЖЕ в дереві).
QUEUE_STALE_FILL = "#F5F5F5"
#: Текст застарілого рядка.
QUEUE_STALE_TXT = "#B0BEC5"
#: Звичайний рядок черги.
QUEUE_FILL = "white"
#: Рамка рядків черги.
QUEUE_BORDER = "#CFCFCF"

# ---------------------------------------------------------------------------
# Палітра: спільні акценти (успадковано від проєкту Флойда–Воршала)
# ---------------------------------------------------------------------------
#: 🟦 заливка / обведення «у фокусі» (інформаційні рамки, підписи).
BLUE_FILL, BLUE_EDGE = "#E3F2FD", "#1976D2"
#: 🟩 заливка / текст «прийнято, покращилося».
GREEN_FILL, GREEN_TXT = "#C8E6C9", "#2E7D32"
#: 🔴 головний акцент (щойно додане ребро, важливі підписи).
PATH_COLOR = "#D32F2F"

# ---------------------------------------------------------------------------
# Палітра: підсвічування кодової панелі (ліва панель «код ↔ граф»)
# ---------------------------------------------------------------------------
# Колір кодує СЕМАНТИКУ гілки, що спрацювала на цьому кроці, а не лише «активність»:
#: 🟨 рядок коду, що виконується зараз (heappop / перевірка умови).
HL_ACTIVE = "#FFF3C4"
#: 🟩 спрацювала гілка «прийняти»: вершина нова, ребро додано до МОД.
#: Навмисно гармонує з TREE_EDGE — зелений рядок коду ↔ зелене ребро на графі.
HL_ADD = "#C8E6C9"
#: 🟥 спрацювала гілка «пропустити»: ребро застаріле (вершина вже в дереві).
HL_SKIP = "#FFCDD2"

# ---------------------------------------------------------------------------
# Палітра: текстові відтінки
# ---------------------------------------------------------------------------
#: Заголовки і другорядні підписи.
HEADER_TXT = "#555"
#: Ще другорядніші підписи (пояснення у формулах).
SUBLABEL_TXT = "#777"
#: Приглушений сірий (підписи, неактивні значення).
MUTED_TXT = "#9aa0a6"
#: Звичайний (темний) текст значень.
TEXT_DARK = "#222"
#: Числа у формулах/підсумках.
TEXT_FORMULA = "#3c4043"
#: Підсумковий результат (без зеленого акценту).
TEXT_RESULT = "#1a1a1a"


def configure_style() -> None:
    """Налаштовує глобальні параметри matplotlib (DPI, шрифт із кирилицею)."""
    plt.rcParams["figure.dpi"] = FIGURE_DPI
    plt.rcParams["font.size"] = BASE_FONT_SIZE
    plt.rcParams["font.family"] = FONT_FAMILY
    # анімації тримають десятки кадрів-фігур водночас до зшивання у GIF —
    # вимикаємо попередження «надто багато відкритих фігур» (їх закриває save_gif)
    plt.rcParams["figure.max_open_warning"] = 0
